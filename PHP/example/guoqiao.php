<?php  
/*
假设某人有100,000现金，每经过一次路口需要进行一次交费，
交费规则为当他现金大于50,000时，每次需要交5%，如果现金小于等于50,000时每次交5,000，
请写一程序计算此人可以经过多少次这个路口。
*/


$total=100000;
$n=0;
while($total>=5000){
	$n++;
	if($total>50000){
		$total=0.95*$total;
	}else{
		$total=$total-5000;
	}
	echo "第",$n,"次过桥，还剩下",$total,"元<br/>";
}


